package br.edu.ifsp.admo.medias;

public class CalculadoraAritmetica {
    public Double calcular(Double valorOne,
                           Double valorTwo,
                           Double valorThree,
                           Double valorFour,
                           Double valorFive){
        return (valorOne + valorTwo + valorThree + valorFour + valorFive ) / 5;
    }
}
